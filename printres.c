#include<stdio.h>
#include<math.h>

void pinfo(char *text){
	printf("----------------------------------------------------------------------------------\n");
	printf("|  %-18s         |   Analasys                                       |\n",text);
	printf("|--------------------------------------------------------------------------------|\n");
	printf("|     N     |   CPU time (T)  |    T/(N*N)     |     T/N*N*N    | T/(N*N*log(N)) |\n");
	printf("|--------------------------------------------------------------------------------|\n");

}

void pres(int N, double T){

	printf("|  %6d   |       %-6.0f    |  %e  |  %e  |  %e  |\n",N,T,T/((double)N*N),T/((double)N*N*N),T/((double)N*N*log(N)));
	printf("|--------------------------------------------------------------------------------|\n");

}

